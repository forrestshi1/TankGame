    public ATank(int x, int y) {
        super(x, y);
    }

    @Override
    public void  run() {
        while (true){
            //让敌人子弹消失后再发射
            if (islive &&  shots.size() <3){
                //创建子弹
                Shot shot = null;
                switch (getDirect()){
                    case 0: //向上
                        shot = new Shot(getX() + 28, getY()-34, 0);
                        break;
                    case 2: //向左
                        shot = new Shot(getX() - 34, getY() + 28, 2);

                        break;
                    case 1: //向下
                        shot = new Shot(getX() + 28, getY() + 90, 1);

                        break;
                    case 3: //向右
                        shot = new Shot(getX()+90, getY() + 28, 3);
                        break;
                }
                shots.add(shot);
                Thread t = new Thread(shot);
                t.start();

            }

            //根据坦克的方向来移动坦克
            switch (getDirect()){
                case 0://向上
                    for (int i = 0; i < 30; i++) {
                        if (getY()>0 &&!isTouchATanks() ){
                            moveUp();
                        }//走30步休眠下再转向
                        try {
                            Thread.sleep(50);
                        } catch (InterruptedException e) {
                            throw new RuntimeException(e);
                        }
                    }
                    break;
                case 1://向下
                    for (int i = 0; i < 30; i++) {
                        if (getY()+60<750&&!isTouchATanks() ){
                            moveDown();
                        }
                        try {
                            Thread.sleep(50);
                        } catch (InterruptedException e) {
                            throw new RuntimeException(e);
                        }
                    }
                    break;
                case 2://向左
                    for (int i = 0; i < 30; i++) {
                        if (getX()>0&&!isTouchATanks() ){
                            moveLeft();
                        }
                        try {
                            Thread.sleep(50);
                        } catch (InterruptedException e) {
                            throw new RuntimeException(e);
                        }
                    }
                    break;
                case 3://向右
                    for (int i = 0; i < 30; i++) {
                        if (getX()+60<1000&&!isTouchATanks() ){
                            moveRight();
                        }
                        try {
                            Thread.sleep(50);
                        } catch (InterruptedException e) {
                            throw new RuntimeException(e);
                        }
                    }
                    break;

            }



            //随机改变方向
            setDirect((int)(Math.random()*4));

            if (islive==false){
                break;
            }
        }
    }
